#include <ctype.h>

int	ft_isprint(int c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || (c == ' ')
		|| (c >= 33 && c <= 64) || (c >= 91 && c <= 96) || (c >= 123
			&& c <= 126));
}
